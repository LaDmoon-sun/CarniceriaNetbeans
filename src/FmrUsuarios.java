    /**
     * Obtiene el ID numérico del Rol basándose en su nombre de la base de datos
     */
    private int obtenerIdRol(String nombreRol) {
        String sql = "SELECT id_rol FROM roles WHERE nombre_rol = ?";
        // Se invoca directamente la clase conexion
        try (Connection con = new conexion().conectar();
             PreparedStatement ps = con.prepareStatement(sql)) {
            ps.setString(1, nombreRol);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    return rs.getInt("id_rol");
                }
            }
        } catch (SQLException e) {
            System.err.println("Error al obtener ID del rol: " + e.getMessage());
        }
        return 2; 
    }
    
