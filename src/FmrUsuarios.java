    private void configurarEventosManuales() {
        // 🔍 Filtro dinámico en tiempo real
        jTextFieldBuscar.addKeyListener(new java.awt.event.KeyAdapter() {
            @Override
            public void keyReleased(java.awt.event.KeyEvent evt) {
                mostrarTabla(jTextFieldBuscar.getText().trim());
            }
        });

        // 鼠标 Al hacer clic en la tabla se mapean los datos a las cajas de texto y combos
        jTableNUR.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int fila = jTableNUR.getSelectedRow();
                if (fila >= 0) {
                    Object idVal = jTableNUR.getValueAt(fila, 0);
                    Object nomVal = jTableNUR.getValueAt(fila, 1);
                    Object usuVal = jTableNUR.getValueAt(fila, 2);
                    Object rolVal = jTableNUR.getValueAt(fila, 3);
                    Object estVal = jTableNUR.getValueAt(fila, 4);
                    
                    if (idVal != null) jTextField1.setText(idVal.toString());
                    if (nomVal != null) jTextField2.setText(nomVal.toString());
                    if (usuVal != null) jTextField3.setText(usuVal.toString());
                    
                    if (rolVal != null) jComboBox1.setSelectedItem(rolVal.toString());
                    if (estVal != null) jComboBox2.setSelectedItem(estVal.toString());
                    
                    jPasswordField1.setText(""); // Por seguridad se limpia el password
                }
            }
        });
    }
    
    public void mostrarTabla(String valorBuscar) {
        DefaultTableModel modelo = new DefaultTableModel(
            new Object[][]{},
            new String[]{"ID", "Nombre Completo", "Usuario", "Rol", "Estado"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; 
            }
        };
        
        jTableNUR.setModel(modelo);
        modelo.setRowCount(0);
        
        String sql = "SELECT u.id_usuario, u.nombre, u.usuario, r.nombre_rol, u.estado "
                   + "FROM usuarios u "
                   + "INNER JOIN roles r ON u.id_rol = r.id_rol "
                   + "WHERE u.nombre LIKE ? OR u.usuario LIKE ? "
                   + "ORDER BY u.id_usuario DESC";
        
        int totalUsuarios = 0;
        
        // Se instancia la clase local directamente en el try-with-resources sin guardarla en propiedad
        try (Connection con = new conexion().conectar();
             PreparedStatement ps = con.prepareStatement(sql)) {
            
            if (con == null) return;
            
            ps.setString(1, "%" + valorBuscar.trim() + "%");
            ps.setString(2, "%" + valorBuscar.trim() + "%");
            
            try (ResultSet rs = ps.executeQuery()) {
                while (rs.next()) {
                    modelo.addRow(new Object[]{
                        rs.getInt("id_usuario"),
                        rs.getString("nombre"),
                        rs.getString("usuario"),
                        rs.getString("nombre_rol"),
                        rs.getString("estado")
                    });
                    totalUsuarios++;
                }
            }
            
            jLabelNusuario.setText(String.valueOf(totalUsuarios));
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar la tabla de usuarios: " + e.getMessage(), "Error SQL", JOptionPane.ERROR_MESSAGE);
        }
    }
