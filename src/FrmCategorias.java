    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
    String nombre = jTextFieldNdeCategoria.getText().trim();
        
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre de la categoría es obligatorio.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String sql = "INSERT INTO categorias (nombre_categoria) VALUES (?)";
        conexion conClase = new conexion();
        
        try (Connection con = conClase.conectar();
             PreparedStatement ps = con.prepareStatement(sql)) {
            
            if (con == null) return;
            ps.setString(1, nombre);
            
            if (ps.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this, "¡Categoría registrada con éxito!");
                jButtonLimpiarActionPerformed(null); // Borra campos y recarga la tabla automáticamente
            }
            
        } catch (SQLException e) {
            if (e.getErrorCode() == 1062) { // Llave única duplicada en MariaDB
                JOptionPane.showMessageDialog(this, "Error: La categoría '" + nombre + "' ya existe.", "Duplicado", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Error SQL: " + e.getMessage(), "Error de Base de Datos", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error General", JOptionPane.ERROR_MESSAGE);
        }    
        
// TODO add your handling code here:
    }//GEN-LAST:event_jButtonGuardarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        String idStr = jTextFieldIdcategoria.getText().trim();
        String nombre = jTextFieldNdeCategoria.getText().trim();
        
        if (idStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecciona primero una categoría de la tabla para poder editarla.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre de la categoría no puede estar vacío.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String sql = "UPDATE categorias SET nombre_categoria = ? WHERE id_categoria = ?";
        conexion conClase = new conexion();
        
        try (Connection con = conClase.conectar();
             PreparedStatement ps = con.prepareStatement(sql)) {
            
            if (con == null) return;
            ps.setString(1, nombre);
            ps.setInt(2, Integer.parseInt(idStr));
            
            if (ps.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this, "Categoría actualizada correctamente.");
                jButtonLimpiarActionPerformed(null); // Refresca y limpia la pantalla
            }
        } catch (SQLException e) {
            if (e.getErrorCode() == 1062) {
                JOptionPane.showMessageDialog(this, "Error: Ya existe otra categoría con el nombre '" + nombre + "'.", "Duplicado", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Error SQL: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error General", JOptionPane.ERROR_MESSAGE);
        }
        
// TODO add your handling code here:
    }//GEN-LAST:event_jButtonEditarActionPerformed

