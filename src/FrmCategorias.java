    public void mostrarCategorias(String valor) {
        DefaultTableModel modelo = new DefaultTableModel(
            new Object[][]{}, 
            new String[]{"ID Categoría", "Nombre de la Categoría"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Garantiza que la JTable sea de SOLO LECTURA
            }
        };
        jTableTCategoria.setModel(modelo);
        modelo.setRowCount(0); 

        String sql = "SELECT id_categoria, nombre_categoria FROM categorias WHERE nombre_categoria LIKE ? ORDER BY id_categoria DESC";
        conexion conClase = new conexion();

        try (Connection con = conClase.conectar();
             PreparedStatement ps = con.prepareStatement(sql)) {

            if (con == null) return;

            ps.setString(1, "%" + valor.trim() + "%");

            try (ResultSet rs = ps.executeQuery()) {
                while (rs.next()) {
                    modelo.addRow(new Object[]{
                        rs.getInt("id_categoria"),
                        rs.getString("nombre_categoria")
                    });
                }
            }
        } catch (Exception e) {
            System.err.println("Error al mapear categorías en la tabla: " + e.getMessage());
        }
    }
    
    private void configurarEventosManuales() {
        // 🔍 Buscador en tiempo real por cada tecla soltada
        jTextFieldBCategoria.addKeyListener(new java.awt.event.KeyAdapter() {
            @Override
            public void keyReleased(java.awt.event.KeyEvent evt) {
                mostrarCategorias(jTextFieldBCategoria.getText().trim());
            }
        });

        // 🖱️ Clic en la tabla para transferir datos a los campos superiores
        jTableTCategoria.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int fila = jTableTCategoria.getSelectedRow();
                if (fila >= 0) {
                    Object idVal = jTableTCategoria.getValueAt(fila, 0);
                    Object nomVal = jTableTCategoria.getValueAt(fila, 1);
                    
                    if (idVal != null && nomVal != null) {
                        jTextFieldIdcategoria.setText(idVal.toString());
                        jTextFieldNdeCategoria.setText(nomVal.toString());
                    }
                }
            }
        });
    }
    
