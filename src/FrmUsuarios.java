    private void ejecutarConsultaBusqueda(String valorBuscar) {
    modeloTabla.setRowCount(0);
    
    // Corregido: r.nombre_rol es el nombre real en tu tabla 'roles'
    String sql = "SELECT u.cedula, u.nombre, u.telefono, u.cargo, u.usuario, u.estado, r.nombre_rol AS rol_nombre "
               + "FROM usuarios u "
               + "INNER JOIN roles r ON u.id_rol = r.id_rol "
               + "WHERE u.nombre LIKE ? OR u.usuario LIKE ? OR u.cedula LIKE ? "
               + "ORDER BY u.nombre ASC";
               
    int totalUsuarios = 0;

    conexion cc = new conexion();
    try (Connection con = cc.conectar();
         PreparedStatement ps = con.prepareStatement(sql)) {

        ps.setString(1, "%" + valorBuscar + "%");
        ps.setString(2, "%" + valorBuscar + "%");
        ps.setString(3, "%" + valorBuscar + "%");
        
        try (ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                Object[] fila = new Object[6];
                fila[0] = rs.getString("cedula");
                fila[1] = rs.getString("nombre");      // Nombre completo de la persona
                fila[2] = rs.getString("telefono");
                fila[3] = rs.getString("cargo");
                
                // Ahora sí traerá de forma estricta "Jefe" o "Empleado" (o como los tengas mapeados)
                String rolObtenido = rs.getString("rol_nombre"); 
                fila[4] = (rolObtenido != null && !rolObtenido.isEmpty()) ? rolObtenido : "Empleado";
                
                fila[5] = rs.getString("estado");

                modeloTabla.addRow(fila);
                totalUsuarios++;
            }
        }
        jLabelNusuario.setText(String.valueOf(totalUsuarios));

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al procesar usuarios: " + e.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
    }
}
    
    private void configurarEventoTabla() {
        jTableNUR.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int filaSeleccionada = jTableNUR.getSelectedRow();
                if (filaSeleccionada >= 0) {
                    
                    int filaModelo = jTableNUR.convertRowIndexToModel(filaSeleccionada);

                    String cedula   = modeloTabla.getValueAt(filaModelo, 0) != null ? modeloTabla.getValueAt(filaModelo, 0).toString().trim() : "";
                    String nombre   = modeloTabla.getValueAt(filaModelo, 1) != null ? modeloTabla.getValueAt(filaModelo, 1).toString().trim() : "";
                    String telefono = modeloTabla.getValueAt(filaModelo, 2) != null ? modeloTabla.getValueAt(filaModelo, 2).toString().trim() : "";
                    String cargo    = modeloTabla.getValueAt(filaModelo, 3) != null ? modeloTabla.getValueAt(filaModelo, 3).toString().trim() : "";
                    String rol      = modeloTabla.getValueAt(filaModelo, 4) != null ? modeloTabla.getValueAt(filaModelo, 4).toString().trim() : "";
                    String estado   = modeloTabla.getValueAt(filaModelo, 5) != null ? modeloTabla.getValueAt(filaModelo, 5).toString().trim() : "";

                    jTextFieldCedula.setText(cedula);
                    jTextFieldNombre.setText(nombre);
                    jTextFieldTelefono.setText(telefono);
                    
                    jComboBoxCargodelaempresa.setSelectedItem(cargo.isEmpty() ? "Jefe" : cargo);
                    jComboBoxEstadoC.setSelectedItem(estado.isEmpty() ? "Activo" : estado);

                    jComboBoxRolsistema.setSelectedIndex(-1);
                    String rolUpper = rol.toUpperCase();
                    if (rolUpper.contains("JEFE")) {
                        jComboBoxRolsistema.setSelectedItem("Jefe");
                    } else {
                        jComboBoxRolsistema.setSelectedItem("Empleado");
                    }

                    if (!cedula.isEmpty()) {
                        recuperarCredencialesUsuario(cedula);
                    }
                }
            }
        });
    }
    
    private void recuperarCredencialesUsuario(String cedula) {
        String sql = "SELECT usuario, clave FROM usuarios WHERE cedula = ?";
        conexion cc = new conexion();
        try (Connection con = cc.conectar();
             PreparedStatement ps = con.prepareStatement(sql)) {
            
            ps.setString(1, cedula);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    jTextFieldUsuario.setText(rs.getString("usuario"));
                    jPasswordFieldContrasena.setText(rs.getString("clave"));
                }
            }
        } catch (SQLException e) {
            System.out.println("Error al recuperar credenciales: " + e.getMessage());
        }
    }
    
