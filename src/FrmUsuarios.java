    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed

   String cedula = jTextFieldCedula.getText().trim();
        String nombre = jTextFieldNombre.getText().trim();
        String telefono = jTextFieldTelefono.getText().trim();
        String cargo = jComboBoxCargodelaempresa.getSelectedItem().toString();
        String usuario = jTextFieldUsuario.getText().trim();
        String clave = new String(jPasswordFieldContrasena.getPassword()).trim();
        String rolSeleccionado = jComboBoxRolsistema.getSelectedItem().toString();
        String estado = jComboBoxEstadoC.getSelectedItem().toString();

        // Validaciones básicas de campos obligatorios marcados con *
        if (cedula.isEmpty() || nombre.isEmpty() || usuario.isEmpty() || clave.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos obligatorios (*)", "Campos Incompletos", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idRol = obtenerIdRolPorNombre(rolSeleccionado);

        String sql = "INSERT INTO usuarios (cedula, nombre, telefono, cargo, usuario, clave, id_rol, estado) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        
        conexion cc = new conexion();
        try (Connection con = cc.conectar();
             PreparedStatement ps = con.prepareStatement(sql)) {
            
            ps.setString(1, cedula);
            ps.setString(2, nombre);
            ps.setString(3, telefono);
            ps.setString(4, cargo);
            ps.setString(5, usuario);
            ps.setString(6, clave);
            ps.setInt(7, idRol);
            ps.setString(8, estado);

            int resultado = ps.executeUpdate();
            if (resultado > 0) {
                JOptionPane.showMessageDialog(this, "Usuario registrado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                limpiarFormulario();
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar el usuario: " + e.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
        } 

    }//GEN-LAST:event_jButtonGuardarActionPerformed

