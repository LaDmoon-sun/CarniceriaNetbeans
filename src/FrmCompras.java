   private void buscarProductoPorCodigo() {
    String entrada = jTextFieldCódigoBarras.getText().trim();
    
    // Si el campo está vacío, asumimos que quiere buscar por nombre de producto
    if (entrada.isEmpty()) {
        String busquedaNombre = JOptionPane.showInputDialog(this, "Ingrese el nombre del producto a buscar:", "Buscador de Productos", JOptionPane.QUESTION_MESSAGE);
        if (busquedaNombre == null || busquedaNombre.trim().isEmpty()) return;
        entrada = busquedaNombre.trim();
    }

    // Primero intentamos buscar de forma exacta por código de barras
    String sql = "SELECT codigo_barras, nombre FROM productos WHERE codigo_barras = ? AND estado = 'Activo'";
    try (Connection con = new conexion().conectar();
         PreparedStatement ps = con.prepareStatement(sql)) {
        ps.setString(1, entrada);
        try (ResultSet rs = ps.executeQuery()) {
            if (rs.next()) {
                jTextFieldCódigoBarras.setText(rs.getString("codigo_barras"));
                jLabelNameProducto.setText(rs.getString("nombre"));
                jTextFieldCantidadKG.requestFocus();
                return; // Encontrado con éxito
            }
        }
    } catch (SQLException e) {
        System.err.println("Error: " + e.getMessage());
    }

    // Si no se encontró por código exacto, realizamos una búsqueda inteligente por coincidencia de nombre
    String sqlLike = "SELECT codigo_barras, nombre, precio_kg FROM productos WHERE nombre LIKE ? AND estado = 'Activo' ORDER BY nombre ASC";
    try (Connection con = new conexion().conectar();
         PreparedStatement ps = con.prepareStatement(sqlLike)) {
        ps.setString(1, "%" + entrada + "%");
        
        try (ResultSet rs = ps.executeQuery()) {
            java.util.List<String> opciones = new java.util.ArrayList<>();
            java.util.List<String> codigos = new java.util.ArrayList<>();
            
            while (rs.next()) {
                codigos.add(rs.getString("codigo_barras"));
                opciones.add(rs.getString("nombre") + " [" + rs.getString("codigo_barras") + "]");
            }
            
            if (opciones.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No se encontraron productos con ese código o nombre.", "Sin resultados", JOptionPane.WARNING_MESSAGE);
                jLabelNameProducto.setText("---");
            } else if (opciones.size() == 1) {
                // Si solo hay una coincidencia, la selecciona automáticamente
                jTextFieldCódigoBarras.setText(codigos.get(0));
                jLabelNameProducto.setText(opciones.get(0).split(" \\[")[0]);
                jTextFieldCantidadKG.requestFocus();
            } else {
                // Si hay múltiples coincidencias, le despliega una lista limpia para elegir el correcto
                String seleccion = (String) JOptionPane.showInputDialog(
                    this, 
                    "Seleccione el producto exacto:", 
                    "Múltiples Coincidencias Encontradas", 
                    JOptionPane.PLAIN_MESSAGE, 
                    null, 
                    opciones.toArray(), 
                    opciones.get(0)
                );
                
                if (seleccion != null) {
                    int index = opciones.indexOf(seleccion);
                    jTextFieldCódigoBarras.setText(codigos.get(index));
                    jLabelNameProducto.setText(seleccion.split(" \\[")[0]);
                    jTextFieldCantidadKG.requestFocus();
                }
            }
        }
    } catch (SQLException e) {
        System.err.println("Error en búsqueda por nombre: " + e.getMessage());
    }
}

    private int obtenerIdProveedor(String nombreProv) {
        String sql = "SELECT id_proveedor FROM proveedores WHERE nombre = ?";
        try (Connection con = new conexion().conectar();
             PreparedStatement ps = con.prepareStatement(sql)) {
            ps.setString(1, nombreProv);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) return rs.getInt("id_proveedor");
            }
        } catch (SQLException e) {
            System.err.println("Error al obtener ID del proveedor: " + e.getMessage());
        }
        return 1; 
    }

    private void calcularTotal() {
        totalCompra = 0.0;
        for (int i = 0; i < modeloDetalle.getRowCount(); i++) {
            totalCompra += (double) modeloDetalle.getValueAt(i, 4);
        }
        jLabelNtOTAL.setText(String.format("%.2f", totalCompra));
    }

