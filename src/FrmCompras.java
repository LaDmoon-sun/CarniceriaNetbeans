    private void configurarBuscadorProveedores() {
      
        jComboBoxProveedor.removeAllItems();
    // Ajustado a tu columna real 'nombre_empresa' de la tabla 'proveedores'
    String sqlProv = "SELECT nombre_empresa FROM proveedores ORDER BY nombre_empresa ASC";
    try (Connection con = new conexion().conectar();
         PreparedStatement ps = con.prepareStatement(sqlProv);
         ResultSet rs = ps.executeQuery()) {
        while (rs.next()) {
            jComboBoxProveedor.addItem(rs.getString("nombre_empresa"));
        }
    } catch (SQLException e) {
        System.err.println("Error al cargar proveedores: " + e.getMessage());
        jComboBoxProveedor.addItem("Proveedor Genérico");
    }

    // Configurar autocompletado interactivo al escribir
    final javax.swing.JTextField tf = (javax.swing.JTextField) jComboBoxProveedor.getEditor().getEditorComponent();
    tf.addKeyListener(new java.awt.event.KeyAdapter() {
        @Override
        public void keyReleased(java.awt.event.KeyEvent e) {
            // Evitar interceptar flechas de navegación
            if (e.getKeyCode() == java.awt.event.KeyEvent.VK_UP || 
                e.getKeyCode() == java.awt.event.KeyEvent.VK_DOWN || 
                e.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
                return;
            }
            
            String texto = tf.getText();
            jComboBoxProveedor.removeAllItems();
            
            String sqlFiltrar = "SELECT nombre_empresa FROM proveedores WHERE nombre_empresa LIKE ? ORDER BY nombre_empresa ASC";
            try (Connection con = new conexion().conectar();
                 PreparedStatement ps = con.prepareStatement(sqlFiltrar)) {
                ps.setString(1, "%" + texto + "%");
                try (ResultSet rs = ps.executeQuery()) {
                    while (rs.next()) {
                        jComboBoxProveedor.addItem(rs.getString("nombre_empresa"));
                    }
                }
            } catch (SQLException ex) {
                System.err.println(ex.getMessage());
            }
            tf.setText(texto);
            jComboBoxProveedor.showPopup();
        }
    });
        
    }
