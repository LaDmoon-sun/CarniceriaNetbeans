    private void jButtonProcesarComprasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProcesarComprasActionPerformed

if (jTableCPCPS.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "El detalle de la compra está vacío.", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String provSel = jComboBoxProveedor.getSelectedItem() != null ? jComboBoxProveedor.getSelectedItem().toString() : "Proveedor Genérico";
        int idProveedor = obtenerIdProveedor(provSel);

        Connection con = null;
        PreparedStatement psCompra = null;
        PreparedStatement psDetalle = null;
        PreparedStatement psStock = null;

        try {
            con = new conexion().conectar();
            con.setAutoCommit(false); // Transacción segura

            // 1. Cabecera
            String sqlCompra = "INSERT INTO compras (id_proveedor, fecha, total) VALUES (?, NOW(), ?)";
            psCompra = con.prepareStatement(sqlCompra, Statement.RETURN_GENERATED_KEYS);
            psCompra.setInt(1, idProveedor);
            psCompra.setDouble(2, totalCompra);
            psCompra.executeUpdate();

            int idCompra = 1;
            try (ResultSet gk = psCompra.getGeneratedKeys()) {
                if (gk.next()) idCompra = gk.getInt(1);
            }

            // 2. Lotes de Detalles y Stock
            String sqlDetalle = "INSERT INTO detalle_compras (id_compra, codigo_barras, cantidad_kg, precio_compra) VALUES (?, ?, ?, ?)";
            String sqlStock = "UPDATE productos SET stock_kg = stock_kg + ? WHERE codigo_barras = ?";

            psDetalle = con.prepareStatement(sqlDetalle);
            psStock = con.prepareStatement(sqlStock);

            for (int i = 0; i < jTableCPCPS.getRowCount(); i++) {
                String cod = jTableCPCPS.getValueAt(i, 0).toString();
                double cant = (double) jTableCPCPS.getValueAt(i, 2);
                double cost = (double) jTableCPCPS.getValueAt(i, 3);

                psDetalle.setInt(1, idCompra);
                psDetalle.setString(2, cod);
                psDetalle.setDouble(3, cant);
                psDetalle.setDouble(4, cost);
                psDetalle.addBatch();

                psStock.setDouble(1, cant);
                psStock.setString(2, cod);
                psStock.addBatch();
            }

            psDetalle.executeBatch();
            psStock.executeBatch();

            con.commit();
            JOptionPane.showMessageDialog(this, "Compra registrada. Inventario actualizado.");
            limpiarFormulario();

        } catch (SQLException e) {
            if (con != null) {
                try { con.rollback(); } catch (SQLException ex) { System.err.println(ex.getMessage()); }
            }
            JOptionPane.showMessageDialog(this, "Error al procesar: " + e.getMessage(), "Error SQL", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (psCompra != null) psCompra.close();
                if (psDetalle != null) psDetalle.close();
                if (psStock != null) psStock.close();
                if (con != null) con.close();
            } catch (SQLException e) { System.err.println(e.getMessage()); }
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButtonProcesarComprasActionPerformed

    private void jButtonAgregaralDetalleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregaralDetalleActionPerformed

String codigo = jTextFieldCódigoBarras.getText().trim();
        String nombre = jLabelNameProducto.getText();
        String cantStr = jTextFieldCantidadKG.getText().trim();
        String costStr = jTextFieldCosto.getText().trim();

        if (codigo.isEmpty() || nombre.equals("---") || cantStr.isEmpty() || costStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe buscar un producto válido, ingresar cantidad y costo.", "Campos incompletos", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            double cantidad = Double.parseDouble(cantStr);
            double costo = Double.parseDouble(costStr);
            double subtotal = cantidad * costo;

            // Agrupar filas del mismo código de producto si ya existe en la tabla
            for (int i = 0; i < jTableCPCPS.getRowCount(); i++) {
                if (jTableCPCPS.getValueAt(i, 0).toString().equals(codigo)) {
                    double cantExistente = (double) jTableCPCPS.getValueAt(i, 2);
                    double nuevoSubtotal = (cantExistente + cantidad) * costo;
                    jTableCPCPS.setValueAt(cantExistente + cantidad, i, 2);
                    jTableCPCPS.setValueAt(costo, i, 3);
                    jTableCPCPS.setValueAt(nuevoSubtotal, i, 4);
                    calcularTotal();
                    
                    // Reset parcial para nueva inserción
                    jTextFieldCódigoBarras.setText("");
                    jLabelNameProducto.setText("---");
                    jTextFieldCantidadKG.setText("");
                    jTextFieldCosto.setText("");
                    jTextFieldCódigoBarras.requestFocus();
                    return;
                }
            }

            // Añadir fila nueva
            modeloDetalle.addRow(new Object[]{codigo, nombre, cantidad, costo, subtotal});
            calcularTotal();

            // Reset parcial
            jTextFieldCódigoBarras.setText("");
            jLabelNameProducto.setText("---");
            jTextFieldCantidadKG.setText("");
            jTextFieldCosto.setText("");
            jTextFieldCódigoBarras.requestFocus();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese números válidos para cantidad y costo.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButtonAgregaralDetalleActionPerformed

