    String[] partesApellidos = apellidosCompletos.split("\\s+");
    if (partesApellidos.length > 0) primerApellido = partesApellidos[0];
    if (partesApellidos.length > 1) {
        StringBuilder sb = new StringBuilder();
        for (int i = 1; i < partesApellidos.length; i++) sb.append(partesApellidos[i]).append(" ");
        segundoApellido = sb.toString().trim();
    }

    // 2. SENTENCIA SQL DE ACTUALIZACIÓN
    String sql = "UPDATE clientes SET nombre=?, segundo_nombre=?, apellido=?, segundo_apellido=?, telefono=?, direccion=? WHERE cedula=?";
    conexion conClase = new conexion();

    try (Connection con = conClase.conectar();
         PreparedStatement ps = con.prepareStatement(sql)) {
        
        if (con == null) return;

        // Mapeamos los parámetros ordenados de las columnas hacia el WHERE
        ps.setString(1, primerNombre);
        ps.setString(2, segundoNombre);
        ps.setString(3, primerApellido);
        ps.setString(4, segundoApellido);
        ps.setString(5, jTextFieldTelefono.getText().trim());
        ps.setString(6, jTextFieldDireccion.getText().trim());
        ps.setString(7, cedula); // Identificador único para el WHERE

        int filasAfectadas = ps.executeUpdate();
        
        if (filasAfectadas > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "¡Datos del cliente actualizados con éxito!");
            mostrarClientes(""); // Refresca tu jTableclientes automáticamente
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No se encontró ningún cliente con esa cédula para modificar.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al editar registro: " + e.getMessage(), "Error SQL", javax.swing.JOptionPane.ERROR_MESSAGE);
    }    
