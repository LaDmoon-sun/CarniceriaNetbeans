    /**
 * Método de apoyo encargado de sincronizar MariaDB con tu jTableclientes
 */
 public void mostrarClientes(String valor) {
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(
        new Object[][]{}, 
        new String[]{"Cédula", "Nombre Completo", "Teléfono"}
    ) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // Solo lectura
        }
    };
    jTableclientes.setModel(modelo);
    modelo.setRowCount(0); // Vacía filas viejas

    // 💡 CONCATENAMOS todos los campos en el WHERE para que coincida con el nombre completo que digita el usuario
    String sql = "SELECT cedula, "
               + "CONCAT(nombre, ' ', IFNULL(segundo_nombre,''), ' ', apellido, ' ', IFNULL(segundo_apellido,'')) AS nombre_completo, "
               + "telefono FROM clientes "
               + "WHERE cedula LIKE ? "
               + "OR CONCAT(nombre, ' ', IFNULL(segundo_nombre,''), ' ', apellido, ' ', IFNULL(segundo_apellido,'')) LIKE ? "
               + "OR telefono LIKE ?";

    conexion conClase = new conexion();

    try (Connection con = conClase.conectar();
         PreparedStatement ps = con.prepareStatement(sql)) {

        // Limpiamos espacios dobles residuales que el usuario pueda escribir por error
        String buscar = "%" + valor.trim().replaceAll("\\s+", " ") + "%";
        
        ps.setString(1, buscar); // Busca por Cédula
        ps.setString(2, buscar); // Busca por Nombre Completo (Une Nombre, Snombre, Apellido, Sapellido)
        ps.setString(3, buscar); // Busca por Teléfono

        try (ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                // Reemplaza múltiples espacios en blanco por uno solo para que se vea limpio en la tabla
                String nombreFormateado = rs.getString("nombre_completo").replaceAll("\\s+", " ").trim();
                
                modelo.addRow(new Object[]{
                    rs.getString("cedula"),
                    nombreFormateado,
                    rs.getString("telefono")
                });
            }
        }
        
        // Sincroniza el contador jLabelNClientesR
        String sqlContar = "SELECT COUNT(*) AS total FROM clientes";
        try (PreparedStatement psC = con.prepareStatement(sqlContar);
             ResultSet rsC = psC.executeQuery()) {
            if (rsC.next()) {
                jLabelNClientesR.setText(String.valueOf(rsC.getInt("total")));
            }
        }

    } catch (Exception e) {
        System.err.println("Error en motor gráfico al listar clientes: " + e.getMessage());
    }
}
    
