    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
// 1. Validar que no dejen los campos principales vacíos (Cédula, Nombre y Apellido)
    String cedula = jTextFieldCedula.getText().trim();
    String nombresCompletos = jTextFieldNombre.getText().trim();
    String apellidosCompletos = jTextFieldApellido.getText().trim();
    
    if (cedula.isEmpty() || nombresCompletos.isEmpty() || apellidosCompletos.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "La Cédula, el Nombre y el Apellido son obligatorios", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // VARIABLES PARA LA BASE DE DATOS
    String primerNombre = "";
    String segundoNombre = ""; // Quedará vacío si solo ingresan un nombre
    String primerApellido = "";
    String segundoApellido = ""; // Quedará vacío si solo ingresan un apellido
    
    // 2. SEPARAR LOS NOMBRES (Buscando espacios en blanco)
    String[] partesNombres = nombresCompletos.split("\\s+");
    if (partesNombres.length > 0) {
        primerNombre = partesNombres[0]; 
    }
    if (partesNombres.length > 1) {
        StringBuilder sb = new StringBuilder();
        for (int i = 1; i < partesNombres.length; i++) {
            sb.append(partesNombres[i]).append(" ");
        }
        segundoNombre = sb.toString().trim();
    }
    
    // 3. SEPARAR LOS APELLIDOS
    String[] partesApellidos = apellidosCompletos.split("\\s+");
    if (partesApellidos.length > 0) {
        primerApellido = partesApellidos[0]; 
    }
    if (partesApellidos.length > 1) {
        StringBuilder sb = new StringBuilder();
        for (int i = 1; i < partesApellidos.length; i++) {
            sb.append(partesApellidos[i]).append(" ");
        }
        segundoApellido = sb.toString().trim();
    }

    // 4. SENTENCIA SQL ACTUALIZADA (Ahora con 7 campos)
    String sql = "INSERT INTO clientes (nombre, segundo_nombre, apellido, segundo_apellido, telefono, direccion, cedula) VALUES (?, ?, ?, ?, ?, ?, ?)";
    
    // Instanciamos tu clase de conexión nativa
    conexion conClase = new conexion(); 
    
    try (Connection con = conClase.conectar();
         PreparedStatement ps = con.prepareStatement(sql)) {
        
        if (con == null) {
            return; 
        }
        
        // Mapeamos los 7 datos en el orden exacto de la consulta SQL
        ps.setString(1, primerNombre);
        ps.setString(2, segundoNombre);
        ps.setString(3, primerApellido);
        ps.setString(4, segundoApellido);
        ps.setString(5, jTextFieldTelefono.getText().trim());
        ps.setString(6, jTextFieldDireccion.getText().trim());
        ps.setString(7, cedula); // <-- Pasamos la cédula al último signo de pregunta '?'
        
        // Ejecutamos la inserción en MariaDB/MySQL
        int filasAfectadas = ps.executeUpdate();
        
        if (filasAfectadas > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "¡Cliente registrado con éxito!");
            
            // 5. LIMPIAR TODOS LOS CAMPOS DE LA INTERFAZ
            jTextFieldCedula.setText("");
            jTextFieldNombre.setText("");
            jTextFieldApellido.setText("");
            jTextFieldTelefono.setText("");
            jTextFieldDireccion.setText("");
            
            // 🔄 6. ACTUALIZAR LA INTERFAZ EN TIEMPO REAL
            // Recarga el jTableclientes reflejando los cambios de forma automática
            mostrarClientes(""); 
        }
        
    } catch (java.sql.SQLException e) {
        // Validación inteligente por si intentan meter una cédula que ya existe (Llave duplicada)
        if (e.getErrorCode() == 1062) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Ya existe un cliente registrado con la cédula " + cedula, "Registro Duplicado", javax.swing.JOptionPane.ERROR_MESSAGE);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error de Base de Datos: " + e.getMessage(), "Error SQL", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error inesperado: " + e.getMessage(), "Error General", javax.swing.JOptionPane.ERROR_MESSAGE);
    } 
        // TODO add your handling code here:
    }//GEN-LAST:event_jButtonGuardarActionPerformed

