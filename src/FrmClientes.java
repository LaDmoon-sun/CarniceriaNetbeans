jTableclientes.addMouseListener(new java.awt.event.MouseAdapter() {
    @Override
    public void mouseClicked(java.awt.event.MouseEvent evt) {
        int fila = jTableclientes.getSelectedRow();
        if (fila >= 0) {
            // 🛡️ Validamos que la celda de la cédula no esté vacía o sea nula
            Object valorCedula = jTableclientes.getValueAt(fila, 0);
            if (valorCedula == null || valorCedula.toString().trim().isEmpty()) {
                return; // Si está vacía (fila por defecto), no hace nada y evita el error
            }
            
            String cedulaSeleccionada = valorCedula.toString().trim();
            
            String sql = "SELECT * FROM clientes WHERE cedula = ?";
            conexion conClase = new conexion();
            try (Connection con = conClase.conectar();
                 PreparedStatement ps = con.prepareStatement(sql)) {
                ps.setString(1, cedulaSeleccionada);
                try (ResultSet rs = ps.executeQuery()) {
                    if (rs.next()) {
                        jTextFieldCedula.setText(rs.getString("cedula"));
                        
                        // Reconstruir nombres y apellidos
                        String nombreCompleto = (rs.getString("nombre") + " " + (rs.getString("segundo_nombre") != null ? rs.getString("segundo_nombre") : "")).trim();
                        String apellidoCompleto = (rs.getString("apellido") + " " + (rs.getString("segundo_apellido") != null ? rs.getString("segundo_apellido") : "")).trim();
                        
                        jTextFieldNombre.setText(nombreCompleto);
                        jTextFieldApellido.setText(apellidoCompleto);
                        jTextFieldTelefono.setText(rs.getString("telefono"));
                        jTextFieldDireccion.setText(rs.getString("direccion"));
                    }
                }
            } catch (Exception e) {
                System.err.println("Error al cargar cliente desde la tabla: " + e.getMessage());
            }
        }
    }
});

